class Solution {
public:
    int kthSmallest(vector<vector<int>>& matrix, int k) {
        int n = matrix.size();
        int l = matrix[0][0], r = matrix[n - 1][n - 1];
        while (r > l){
            int mid = (l + r) >> 1;
            int cnt = 0, rbound = -1;
            for (int i = n - 1; i >= 0; --i){
                while (rbound < n - 1 && matrix[i][rbound + 1] <= mid)
                    ++rbound;
                cnt += rbound + 1;
            }
            if (cnt >= k) r = mid;
            else l = mid + 1;
        }
        return l;
    }
};

